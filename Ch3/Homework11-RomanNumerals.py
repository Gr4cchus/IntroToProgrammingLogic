# Roman Numerals
myInput = int(input('Enter a number within the range of 1 - 10: '))
if myInput == 1:
    print(myInput, 'is I in roman numerals.')
elif myInput == 2:
    print(myInput, 'is II in roman numerals.')
elif myInput == 3:
    print(myInput, 'is III in roman numerals.')
elif myInput == 4:
    print(myInput, 'is IV in roman numerals.')
elif myInput == 5:
    print(myInput, 'is V in roman numerals.')
elif myInput == 6:
    print(myInput, 'is VI in roman numerals.')
elif myInput == 7:
    print(myInput, 'is VII in roman numerals.')
elif myInput == 8:
    print(myInput, 'is VIII in roman numerals.')
elif myInput == 9:
    print(myInput, 'is IX in roman numerals.')
elif myInput == 10:
    print(myInput, 'is X in roman numerals.')
else:
    print('Number outside of the required range.')
